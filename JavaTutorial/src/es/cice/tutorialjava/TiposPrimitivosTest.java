package es.cice.tutorialjava;

public class TiposPrimitivosTest {

	public static void main(String[] args) {
		
		//Tipos primitivos
		int x = 67;
		short s = 34;
		
		float f = 56.7f;
		double d = 98.7;
		
		boolean b = true;
		
		char c = 'r';
		
		//Objetos
		String str = "Jean Carlos";
		
		
		System.out.println("valor de c: " + c);
		System.out.println("valor de str: " + str);
		System.out.println("valor de x: " + x);
		
		x = 0xff56;
		
		System.out.println("valor de x: " + x);
		
		c = '\t';
		System.out.println(c + "Jean Carlos");
		
	}

}
